const mongoose=require('mongoose');


const users=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    }
},{timestamps:true});

const Users=mongoose.model('users',users)

module.exports=Users